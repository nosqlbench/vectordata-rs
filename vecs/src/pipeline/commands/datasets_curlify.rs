//! Pipeline command: generate curl download scripts for remote datasets.
//!
//! Reads a dataset.yaml and generates a bash script with curl commands to
//! download the dataset facets, optionally using HTTP range requests for
//! partial downloads.
//!
//! Equivalent to the Java `CMD_datasets_curlify` command.

use std::path::{Path, PathBuf};
use std::time::Instant;

use crate::pipeline::command::{
    CommandOp, CommandResult, OptionDesc, Options, Status, StreamContext,
};

/// Pipeline command: generate curl commands for dataset download.
pub struct DatasetsCurlifyOp;

pub fn factory() -> Box<dyn CommandOp> {
    Box::new(DatasetsCurlifyOp)
}

impl CommandOp for DatasetsCurlifyOp {
    fn command_path(&self) -> &str {
        "datasets curlify"
    }

    fn execute(&mut self, options: &Options, ctx: &mut StreamContext) -> CommandResult {
        let start = Instant::now();

        let path_str = options.get("path").unwrap_or(".");
        let path = resolve_path(path_str, &ctx.workspace);
        let output_dir = options
            .get("output")
            .map(|s| resolve_path(s, &ctx.workspace));

        // Find dataset.yaml
        let yaml_path = if path.is_file() {
            path.clone()
        } else {
            path.join("dataset.yaml")
        };

        if !yaml_path.exists() {
            return error_result(
                format!("dataset.yaml not found at {}", yaml_path.display()),
                start,
            );
        }

        let content = match std::fs::read_to_string(&yaml_path) {
            Ok(s) => s,
            Err(e) => return error_result(format!("failed to read: {}", e), start),
        };

        let config: serde_yaml::Value = match serde_yaml::from_str(&content) {
            Ok(v) => v,
            Err(e) => return error_result(format!("failed to parse: {}", e), start),
        };

        let dataset_name = config
            .get("name")
            .and_then(|v| v.as_str())
            .unwrap_or("dataset");
        let dataset_dir = yaml_path.parent().unwrap_or(Path::new("."));

        // Determine output directory for downloads
        let out_dir = output_dir.unwrap_or_else(|| dataset_dir.to_path_buf());

        let mut script = String::new();
        script.push_str("#!/bin/bash\n");
        script.push_str(&format!("# Download script for dataset: {}\n", dataset_name));
        script.push_str(&format!("# Generated by vecs datasets curlify\n\n"));
        script.push_str(&format!("OUTDIR=\"{}\"\n", out_dir.display()));
        script.push_str("mkdir -p \"$OUTDIR\"\n\n");

        let mut file_count = 0;

        // Process views from the default profile
        if let Some(profiles) = config.get("profiles").and_then(|v| v.as_mapping()) {
            let default_key = serde_yaml::Value::String("default".to_string());
            let profile = profiles.get(&default_key).or_else(|| profiles.values().next());
            if let Some(profile_map) = profile.and_then(|v| v.as_mapping()) {
                // Check if there's a base URL in upstream
                let base_url = config
                    .get("upstream")
                    .and_then(|u| u.get("base_url"))
                    .and_then(|s| s.as_str());

                for (name, entry) in profile_map {
                    let view_name = name.as_str().unwrap_or("?");
                    if view_name == "maxk" {
                        continue;
                    }

                    // Extract source path from the view entry (string or map)
                    let source_path = if let Some(s) = entry.as_str() {
                        let s = s.split('[').next().unwrap_or(s);
                        let s = s.split('(').next().unwrap_or(s);
                        Some(s.to_string())
                    } else if let Some(m) = entry.as_mapping() {
                        m.get(&serde_yaml::Value::String("source".to_string()))
                            .or_else(|| m.get(&serde_yaml::Value::String("path".to_string())))
                            .and_then(|v| v.as_str())
                            .map(|s| s.to_string())
                    } else {
                        None
                    };

                    if let Some(ref path) = source_path {
                        // Check if source path is a URL
                        if path.starts_with("http://") || path.starts_with("https://") {
                            script.push_str(&format!("# View: {}\n", view_name));
                            script.push_str(&format!(
                                "curl -L -C - -o \"$OUTDIR/{}\" \\\n  \"{}\"\n\n",
                                path.rsplit('/').next().unwrap_or(path), path
                            ));
                            file_count += 1;
                        } else if let Some(base) = base_url {
                            script.push_str(&format!("# View: {}\n", view_name));
                            script.push_str(&format!(
                                "curl -L -C - -o \"$OUTDIR/{}\" \\\n  \"{}/{}\"\n\n",
                                path, base, path
                            ));
                            file_count += 1;
                        }
                    }
                }
            }
        }

        if file_count == 0 {
            eprintln!("No downloadable views found (no URLs in sources)");
            return CommandResult {
                status: Status::Warning,
                message: "no downloadable views".to_string(),
                produced: vec![],
                elapsed: start.elapsed(),
            };
        }

        script.push_str(&format!("echo \"Downloaded {} file(s) to $OUTDIR\"\n", file_count));

        // Write script
        let script_path = dataset_dir.join(format!("download-{}.sh", dataset_name));
        if let Err(e) = std::fs::write(&script_path, &script) {
            return error_result(format!("failed to write script: {}", e), start);
        }

        eprintln!("Wrote download script: {}", script_path.display());
        eprintln!("  {} curl command(s)", file_count);
        eprintln!("  Run with: bash {}", script_path.display());

        CommandResult {
            status: Status::Ok,
            message: format!("{} download commands generated", file_count),
            produced: vec![script_path],
            elapsed: start.elapsed(),
        }
    }

    fn describe_options(&self) -> Vec<OptionDesc> {
        vec![
            OptionDesc {
                name: "path".to_string(),
                type_name: "Path".to_string(),
                required: false,
                default: Some(".".to_string()),
                description: "Dataset directory or dataset.yaml path".to_string(),
            },
            OptionDesc {
                name: "output".to_string(),
                type_name: "Path".to_string(),
                required: false,
                default: None,
                description: "Output directory for downloads".to_string(),
            },
        ]
    }
}

fn resolve_path(path_str: &str, workspace: &Path) -> PathBuf {
    let p = PathBuf::from(path_str);
    if p.is_absolute() { p } else { workspace.join(p) }
}

fn error_result(message: String, start: Instant) -> CommandResult {
    CommandResult {
        status: Status::Error,
        message,
        produced: vec![],
        elapsed: start.elapsed(),
    }
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::pipeline::command::StreamContext;
    use crate::pipeline::progress::ProgressLog;
    use indexmap::IndexMap;

    fn test_ctx(dir: &Path) -> StreamContext {
        StreamContext {
            workspace: dir.to_path_buf(),
            scratch: dir.join(".scratch"),
            cache: dir.join(".cache"),
            defaults: IndexMap::new(),
            dry_run: false,
            progress: ProgressLog::new(),
            threads: 1,
            step_id: String::new(),
        }
    }

    #[test]
    fn test_curlify_with_urls() {
        let tmp = tempfile::tempdir().unwrap();
        let ws = tmp.path();
        let mut ctx = test_ctx(ws);

        let yaml = r#"
name: test-dataset
profiles:
  default:
    base_vectors: "https://example.com/base.fvec"
    query_vectors: "https://example.com/query.fvec"
"#;
        std::fs::write(ws.join("dataset.yaml"), yaml).unwrap();

        let mut opts = Options::new();
        opts.set("path", ws.to_string_lossy().to_string());
        let mut op = DatasetsCurlifyOp;
        let result = op.execute(&opts, &mut ctx);
        assert_eq!(result.status, Status::Ok, "curlify failed: {}", result.message);
        assert!(result.message.contains("2 download"));

        let script = std::fs::read_to_string(ws.join("download-test-dataset.sh")).unwrap();
        assert!(script.contains("curl"));
        assert!(script.contains("base.fvec"));
        assert!(script.contains("query.fvec"));
    }

    #[test]
    fn test_curlify_no_urls() {
        let tmp = tempfile::tempdir().unwrap();
        let ws = tmp.path();
        let mut ctx = test_ctx(ws);

        let yaml = "name: local\nprofiles:\n  default:\n    base_vectors: base.fvec\n";
        std::fs::write(ws.join("dataset.yaml"), yaml).unwrap();

        let mut opts = Options::new();
        opts.set("path", ws.to_string_lossy().to_string());
        let mut op = DatasetsCurlifyOp;
        let result = op.execute(&opts, &mut ctx);
        assert_eq!(result.status, Status::Warning);
    }
}
