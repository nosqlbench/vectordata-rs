//! `<binary> datasets curlify` — generate curl download scripts for a dataset.

use std::path::Path;
// `PathBuf` only appears in the `cli`-gated `CurlifyArgs` fields.
#[cfg(feature = "cli")]
use std::path::PathBuf;

/// Shared clap-derived argument struct for `<binary> datasets
/// curlify`. Both the `vectordata` and `veks` binaries import this.
#[cfg(feature = "cli")]
#[derive(Debug, veks_completion_derive::VeksCli)]
pub struct CurlifyArgs {
    /// Dataset directory or path to dataset.yaml
    #[arg(default_value = ".")]
    pub path: PathBuf,
    /// Output directory for downloads (default: same as dataset directory)
    #[arg(long)]
    pub output: Option<PathBuf>,
}

/// Drive `curlify::run` from a parsed [`CurlifyArgs`].
#[cfg(feature = "cli")]
pub fn run_args(args: CurlifyArgs) -> i32 {
    run(&args.path, args.output.as_deref())
}

pub fn run(path: &Path, output: Option<&Path>) -> i32 {
    let yaml_path = if path.is_file() {
        path.to_path_buf()
    } else {
        path.join("dataset.yaml")
    };

    if !yaml_path.exists() {
        eprintln!("error: dataset.yaml not found at {}", yaml_path.display());
        eprintln!("  (run this command from a dataset directory, or pass the path as an argument)");
        return 1;
    }

    let content = match std::fs::read_to_string(&yaml_path) {
        Ok(c) => c,
        Err(e) => {
            eprintln!("error: failed to read {}: {}", yaml_path.display(), e);
            return 1;
        }
    };

    let config: serde_yaml::Value = match serde_yaml::from_str(&content) {
        Ok(v) => v,
        Err(e) => {
            eprintln!("error: failed to parse {}: {}", yaml_path.display(), e);
            return 1;
        }
    };

    let dataset_name = config
        .get("name")
        .and_then(|v| v.as_str())
        .unwrap_or("dataset");
    let dataset_dir = yaml_path.parent().unwrap_or(Path::new("."));
    let out_dir = output.unwrap_or(dataset_dir);

    let mut script = String::new();
    script.push_str("#!/bin/bash\n");
    script.push_str(&format!("# Download script for dataset: {}\n", dataset_name));
    script.push_str("# Generated by vectordata datasets curlify\n\n");
    script.push_str(&format!("OUTDIR=\"{}\"\n", out_dir.display()));
    script.push_str("mkdir -p \"$OUTDIR\"\n\n");

    let mut file_count = 0;

    if let Some(profiles) = config.get("profiles").and_then(|v| v.as_mapping()) {
        let default_key = serde_yaml::Value::String("default".to_string());
        let profile = profiles
            .get(&default_key)
            .or_else(|| profiles.values().next());
        if let Some(profile_map) = profile.and_then(|v| v.as_mapping()) {
            let base_url = config
                .get("upstream")
                .and_then(|u| u.get("base_url"))
                .and_then(|s| s.as_str());

            for (name, entry) in profile_map {
                let view_name = name.as_str().unwrap_or("?");
                if view_name == "maxk" {
                    continue;
                }

                let source_path = extract_view_path(entry);
                if let Some(ref path) = source_path {
                    if path.starts_with("http://") || path.starts_with("https://") {
                        script.push_str(&format!("# View: {}\n", view_name));
                        script.push_str(&format!(
                            "curl -L -C - -o \"$OUTDIR/{}\" \\\n  \"{}\"\n\n",
                            path.rsplit('/').next().unwrap_or(path),
                            path
                        ));
                        file_count += 1;
                    } else if let Some(base) = base_url {
                        script.push_str(&format!("# View: {}\n", view_name));
                        script.push_str(&format!(
                            "curl -L -C - -o \"$OUTDIR/{}\" \\\n  \"{}/{}\"\n\n",
                            path, base, path
                        ));
                        file_count += 1;
                    }
                }
            }
        }
    }

    if file_count == 0 {
        println!("No downloadable views found (no URLs in sources)");
        return 0;
    }

    script.push_str(&format!(
        "echo \"Downloaded {} file(s) to $OUTDIR\"\n",
        file_count
    ));

    let script_path = dataset_dir.join(format!("download-{}.sh", dataset_name));
    if let Err(e) = std::fs::write(&script_path, &script) {
        eprintln!("error: failed to write {}: {}", script_path.display(), e);
        return 1;
    }

    println!("Wrote download script: {}", rel_display(&script_path));
    println!("  {} curl command(s)", file_count);
    println!("  Run with: bash {}", rel_display(&script_path));
    0
}

fn extract_view_path(entry: &serde_yaml::Value) -> Option<String> {
    if let Some(s) = entry.as_str() {
        let s = s.split('[').next().unwrap_or(s);
        let s = s.split('(').next().unwrap_or(s);
        Some(s.to_string())
    } else if let Some(m) = entry.as_mapping() {
        m.get(&serde_yaml::Value::String("source".to_string()))
            .or_else(|| m.get(&serde_yaml::Value::String("path".to_string())))
            .and_then(|v| v.as_str())
            .map(|s| s.to_string())
    } else {
        None
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn curlify_with_urls() {
        let tmp = tempfile::tempdir().unwrap();
        let yaml = r#"
name: test-dataset
profiles:
  default:
    base_vectors: "https://example.com/base.fvec"
    query_vectors: "https://example.com/query.fvec"
"#;
        std::fs::write(tmp.path().join("dataset.yaml"), yaml).unwrap();

        run(tmp.path(), None);

        let script_path = tmp.path().join("download-test-dataset.sh");
        assert!(script_path.exists());
        let script = std::fs::read_to_string(script_path).unwrap();
        assert!(script.contains("curl"));
        assert!(script.contains("base.fvec"));
        assert!(script.contains("query.fvec"));
    }
}

/// Display a path relative to the current working directory.
/// Falls back to the full path if stripping fails. Inlined here
/// instead of imported because the canonical home for this
/// helper lives in the veks `check` module — a vectordata-side
/// import would create a circular dep.
fn rel_display(path: &std::path::Path) -> String {
    if let Ok(cwd) = std::env::current_dir() {
        path.strip_prefix(&cwd)
            .map(|r| {
                let s = r.to_string_lossy().to_string();
                if s.is_empty() { ".".to_string() } else { s }
            })
            .unwrap_or_else(|_| path.to_string_lossy().to_string())
    } else {
        path.to_string_lossy().to_string()
    }
}
