/// Measure single-threaded write throughput.
///
/// The file is created and the writer is opened in the setup phase
/// (outside the timed region) so that only the `add_record` loop is
/// measured. `finish()` (which writes the pages page and flushes) is
/// likewise excluded by not calling it inside the timed closure — the
/// writer is simply dropped after the record loop.
fn bench_write(c: &mut Criterion) {
    let mut group = c.benchmark_group("write");
    group.sample_size(10);

    for count in [1_000_000u64, 10_000_000] {
        group.throughput(Throughput::Elements(count));
        group.bench_with_input(
            BenchmarkId::new("records", human(count)),
            &count,
            |b, &count| {
                b.iter_batched(
                    || {
                        let tmp = NamedTempFile::new().unwrap();
                        let path = tmp.path().to_path_buf();
                        let writer = SlabWriter::new(&path, WriterConfig::default()).unwrap();
                        (tmp, writer)
                    },
                    |(_tmp, mut writer)| {
                        for i in 0..count {
                            writer.add_record(&make_record(i)).unwrap();
                        }
                    },
                    BatchSize::PerIteration,
                );
            },
        );
    }
    group.finish();
}

/// Measure single-threaded sequential read throughput via `iter()`.
///
/// The file is opened in the setup phase (outside the timed region) so
/// that only the page-by-page deserialization and record extraction are
/// measured. Uses `iter_batched` with `LargeInput` batch size since each
/// setup allocates a `SlabReader` with its file descriptor and in-memory
/// pages-page index.
fn bench_sequential_read(c: &mut Criterion) {
    let mut group = c.benchmark_group("sequential_read");
    group.sample_size(10);

    for count in [1_000_000u64, 10_000_000] {
        let (_tmp, path) = prepare_file(count);

        group.throughput(Throughput::Elements(count));
        group.bench_with_input(
            BenchmarkId::new("records", human(count)),
            &count,
            |b, &_count| {
                b.iter_batched(
                    || SlabReader::open(&path).unwrap(),
                    |reader| {
                        let records = reader.iter().unwrap();
                        assert!(!records.is_empty());
                    },
                    BatchSize::LargeInput,
                );
            },
        );
    }
    group.finish();
}

/// Measure single-threaded random-access read throughput via `get()`.
///
/// 10 000 pseudo-random ordinals (deterministic, using prime-modular
/// scatter) are looked up against a pre-built file. The file is opened
/// in the setup phase so that only the binary-search index lookup plus
/// single-page deserialization cost is measured per `get()` call.
fn bench_random_read(c: &mut Criterion) {
    let mut group = c.benchmark_group("random_read");
    group.sample_size(10);

    for count in [1_000_000u64, 10_000_000] {
        let (_tmp, path) = prepare_file(count);

        let sample_count = 10_000u64;
        let ordinals: Vec<i64> = (0..sample_count)
            .map(|i| ((i * 7919) % count) as i64)
            .collect();

        group.throughput(Throughput::Elements(sample_count));
        group.bench_with_input(
            BenchmarkId::new(
                format!("{}_gets_in", human(sample_count)),
                human(count),
            ),
            &ordinals,
            |b, ordinals| {
                b.iter_batched(
                    || SlabReader::open(&path).unwrap(),
                    |reader| {
                        for &ord in ordinals {
                            reader.get_ref(ord).unwrap();
                        }
                    },
                    BatchSize::LargeInput,
                );
            },
        );
    }
    group.finish();
}

