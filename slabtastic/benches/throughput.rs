/// Measure concurrent random-access read scaling (1/2/4/8/16 threads).
///
/// All threads share the same pre-built 10 M-record file, each with its
/// own `SlabReader` and file descriptor. Readers are pre-opened in the
/// setup phase so that only the `get()` calls are timed.
///
/// Each thread performs 100 000 random `get()` calls using a
/// deterministic prime-modular scatter (each thread gets a distinct seed
/// so access patterns don't overlap). The 1-thread case establishes a
/// single-threaded baseline; comparing it against the multi-thread cases
/// shows scaling behavior.
///
/// Throughput is reported as aggregate gets/s across all threads.
///
/// **Note:** each `get()` reads the containing page and extracts the
/// single record via zero-copy offset lookup (no full page
/// deserialization). Per-get cost is dominated by the seek + read
/// syscalls rather than the index lookup.
fn bench_concurrent_random_read(c: &mut Criterion) {
    let mut group = c.benchmark_group("concurrent_random_read");
    group.sample_size(10);

    let record_count = 10_000_000u64;
    let (_tmp, path) = prepare_file(record_count);
    let reads_per_thread = 100_000u64;

    for n_threads in [1, 2, 4, 8, 16] {
        let total_reads = reads_per_thread * n_threads as u64;
        group.throughput(Throughput::Elements(total_reads));
        group.bench_with_input(
            BenchmarkId::new(
                format!(
                    "{}t_x_{}_gets_in_{}",
                    n_threads,
                    human(reads_per_thread),
                    human(record_count),
                ),
                human(total_reads),
            ),
            &n_threads,
            |b, &n_threads| {
                b.iter_batched(
                    || {
                        (0..n_threads)
                            .map(|_| SlabReader::open(&path).unwrap())
                            .collect::<Vec<_>>()
                    },
                    |readers| {
                        if readers.len() == 1 {
                            // Single-thread: avoid thread-spawn overhead entirely
                            let reader = readers.into_iter().next().unwrap();
                            for i in 0..reads_per_thread {
                                let ordinal =
                                    (i.wrapping_mul(7919) % record_count) as i64;
                                reader.get_ref(ordinal).unwrap();
                            }
                        } else {
                            let handles: Vec<_> = readers
                                .into_iter()
                                .enumerate()
                                .map(|(t, reader)| {
                                    thread::spawn(move || {
                                        let seed = (t as u64).wrapping_mul(6271);
                                        for i in 0..reads_per_thread {
                                            let ordinal =
                                                ((i.wrapping_mul(7919).wrapping_add(seed))
                                                    % record_count)
                                                    as i64;
                                            reader.get_ref(ordinal).unwrap();
                                        }
                                    })
                                })
                                .collect();
                            for h in handles {
                                h.join().unwrap();
                            }
                        }
                    },
                    BatchSize::LargeInput,
                );
            },
        );
    }
    group.finish();
}

/// Measure concurrent sequential read scaling (2/4/8 threads).
///
/// All threads share the same pre-built 1 M-record file and each
/// performs a full `iter()`. Readers are pre-opened in the setup phase
/// so that only the page-by-page iteration is timed. Throughput is
