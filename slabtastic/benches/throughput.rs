/// Measure concurrent independent-file write scaling (2/4/8 threads).
///
/// Each thread writes to its own pre-created temp file with a
/// pre-opened writer. Writers are created in the setup phase so that
/// only the `add_record` loops are timed. `finish()` is excluded — the
/// writers are dropped after the record loop. Throughput is reported as
/// the aggregate record count across all threads per second.
fn bench_concurrent_write(c: &mut Criterion) {
    let mut group = c.benchmark_group("concurrent_write");
    group.sample_size(10);

    let records_per_thread = 1_000_000u64;

    for n_threads in [2, 4, 8] {
        let total = records_per_thread * n_threads as u64;
        group.throughput(Throughput::Elements(total));
        group.bench_with_input(
            BenchmarkId::new(
                format!(
                    "{}t_x_{}_records",
                    n_threads,
                    human(records_per_thread),
                ),
                human(total),
            ),
            &n_threads,
            |b, &n_threads| {
                b.iter_batched(
                    || {
                        (0..n_threads)
                            .map(|_| {
                                let tmp = NamedTempFile::new().unwrap();
                                let path = tmp.path().to_path_buf();
                                let writer =
                                    SlabWriter::new(&path, WriterConfig::default()).unwrap();
                                (tmp, writer)
                            })
                            .collect::<Vec<_>>()
                    },
                    |writers| {
                        let handles: Vec<_> = writers
                            .into_iter()
                            .enumerate()
                            .map(|(t, (_tmp, mut writer))| {
                                thread::spawn(move || {
                                    let base = t as u64 * records_per_thread;
                                    for i in 0..records_per_thread {
                                        writer.add_record(&make_record(base + i)).unwrap();
                                    }
                                    // Keep _tmp alive until writes are done
                                    drop(_tmp);
                                })
                            })
                            .collect();
                        for h in handles {
                            h.join().unwrap();
                        }
                    },
                    BatchSize::PerIteration,
                );
            },
        );
    }
    group.finish();
}

